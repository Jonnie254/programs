    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        if (jTextField10.getText().isEmpty() || jComboBox2.getItemCount() == 0) {
            JOptionPane.showMessageDialog(null, "Student id or semester number is missing");
        } else {
            int id = course.getMax();
            int sid = Integer.parseInt(jTextField10.getText());
            int semesterNo = Integer.parseInt(jComboBox2.getSelectedItem().toString());

            // store courses in an array
            String[] courses = {
                jComboBox3.getSelectedItem().toString(),
                jComboBox4.getSelectedItem().toString(),
                jComboBox7.getSelectedItem().toString(),
                jComboBox8.getSelectedItem().toString(),
                jComboBox9.getSelectedItem().toString(),};
            if (course.isSemesterExists(sid, semesterNo)) {
                JOptionPane.showMessageDialog(this, "This student has already taken the semester " + semesterNo);
            } else {
                boolean courseExists = false;

                // loop through the courses
                for (int i = 0; i < courses.length; i++) {
                    if (course.isCourseExists(sid, "course" + (i + 1), courses[i])) {
                        JOptionPane.showMessageDialog(this, "This student has already taken " + courses[i] + "course");
                        courseExists = true;
                        break;
                    }
                }
                if (!courseExists) {
                    course.insert(id, sid, semesterNo, courses[0], courses[1], courses[2], courses[3], courses[4]);
                    jTable2.setModel(new DefaultTableModel(null, new Object[]{
                        " ID",
                        "Student's ID",
                        "Semester",
                        "course 1",
                        "course 2",
                        "course 3",
                        "course 4",
                        "course 5",}));
                    course.getCourseValue(jTable2, "");
                    clearCourse();
                }
            }
        }
    }//GEN-LAST:event_jButton13ActionPerformed

