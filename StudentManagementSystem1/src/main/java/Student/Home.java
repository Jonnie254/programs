                        String course2 = jTextCourse2.getText();
                        String course3 = jTextCourse3.getText();
                        String course4 = jTextCourse4.getText();
                        String course5 = jTextCourse5.getText();
                        double averange = (score1 + score2 + score3 + score4 + score5) / 5;
                        nf.setMaximumFractionDigits(2);
                        score.insert(id, sid, semesterNo, course1, course2, course3, course4, course5, score1, score2, score3, score4, score5, Double.parseDouble(nf.format(averange)));
                        jTable5.setModel(new DefaultTableModel(null, new Object[]{
                            " ID",
                            "Student's ID",
                            "Semester",
                            "course 1", "score 1",
                            "course 2", "score 2",
                            "course 3", "score 3",
                            "course 4", "score 4",
                            "course 5", "score 5",
                            "averange"
                        }));
                        score.getScoreValue(jTable5, "");
